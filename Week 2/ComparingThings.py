#Comparators

#This returns false.
print(10<2)

#This returns false.
print('cat'=='dog')

#This returns true.
print('cat'=='cat')

#This returns true.
print(1==1.0)

#This returns true.
print(1 != 2.0)

#This returns false.
print(1 == '2')

#This returns a TypeError, because we have two data types that are not compatible.
print(1 < '2')